#IMPORTING NECESSARY LIBRARISES
import numpy as np #used for matrix calculations

#MATRIX A
a=np.array([[1,2,3],[4,5,6]])#array creation

print("array elements are:")
print(a)#displaying the created matrix/array

print("type of array is:",type(a))#data type of array:type(a)
print("shape of array is:",a.shape)#shape of array:a.shape
print("size of array is:",a.size)#size of array:a.size

